package exception

/**
 *
 * @author DELL
 */
class MessageException extends RuntimeException{
	public MessageException(){
		  super();
		  }
		    
		  public MessageException(String msg){
		  super(msg);
		  }
		    
		  public MessageException(String msg, Throwable cause){
		  super(msg, cause);
		  }
		    
		  public MessageException(Throwable cause){
		  super(cause);
		  }
}

